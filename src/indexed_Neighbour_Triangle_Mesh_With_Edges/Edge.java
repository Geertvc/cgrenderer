package indexed_Neighbour_Triangle_Mesh_With_Edges;

/**
 * Class representing an edge of a triangle in the mesh structure.
 * 
 * @author Geert Van Campenhout
 * @version 1.0
 */
public class Edge {
	//index of a adjacent triangle
	public int triangle;
	//index of the edge in the triangle (0, 1 of 2)
	public int i;
	
	/** Initializes an edge with the given index of an adjacent triangle and his index in the triangle.
	 * 
	 * @param triangle	index of an adjacent triangle.
	 * @param i	index of the edge in the triangle (=0, 1 or 2).
	 */
	public Edge(int triangle, int i){
		this.triangle = triangle;
		this.i = i;
	}
}
